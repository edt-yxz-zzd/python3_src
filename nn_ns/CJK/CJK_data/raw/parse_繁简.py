def _iter_parse_fnlp_map_txt_path(fnlp_map_txt_path, *, encoding):
    with open(fnlp_map_txt_path, "rt", encoding=encoding) as fin:
        for line in fin:
            line = line.strip()
            繁, 简 = line.split("\t")
            yield 简, 繁

