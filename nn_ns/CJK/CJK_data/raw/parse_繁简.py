    Trad2Simp_Table = d['Trad2Simp_Table']

    s = set()
    def _add(si, ti):
        s.add((chr(si), chr(ti)))
    for si, ti in Simp2Trad_Table.items():
        _add(si, ti)
    for ti, si in Trad2Simp_Table.items():
        _add(si, ti)
    return s

