def _parse_非对称繁简字(非对称繁简字):
# ￥—替换为 常见繁体字
# 95
# !!!:11、当（!當噹）
# !!!:16、恶（~噁）
# !!!:21、干（~幹乾）
# 30
# !!!@:（110）、罗（!羅囉）
# 39
# !!!&:1、呆（￥獃）
# !!!&:15、泪（~淚）#
# 28
# !!!$:16、凄（￥淒悽）
# !!!$:26、伫（~佇竚）#?
# 21?25?24
# !!!$:2、雕（!鵰凋彫）
    非对称繁简字
    txt = 非对称繁简字
    pattern = r"^[^\s、]+、(?P<一简>\w)（(?P<多繁>[!~]\w+)）(?P<problem>(?:[#][?])?)$"
    head_count_pairs = [
            ("!!!:", 95)
            ,("!!!@:", 30)
            ,("!!!&:", 39)
            ,("!!!$:", 28+24)
            ]

    rex = re.compile(pattern)
    all_pairs = []
    for head, count in head_count_pairs:
        it = _iter_line_tails(head, txt)
        pairs = []
        for tail in it:
            m = rex.fullmatch(tail)
            assert m is not None
            if m["problem"]:
                print_err(tail)
            p = m["一简"], m["多繁"]
            pairs.append(p)
        assert len(pairs) == count
        all_pairs += pairs

    pairs = all_pairs
    s = set() # {(简,繁)}
    for 简, 多繁 in pairs:
        z = 简 if 多繁[0]=='~' else ''
        多繁 = z + 多繁[1:]
        assert len(多繁) >= 2
        for 繁 in 多繁:
            s.add((简,繁))
    return s


